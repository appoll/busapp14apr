package com.models;

public class Frequency {

	 String trip_id;
	    String start_time;
	    String end_time;
	    String headway_secs;
	    String exact_times;
		public String getTrip_id() {
			return trip_id;
		}
		public void setTrip_id(String trip_id) {
			this.trip_id = trip_id;
		}
		public String getStart_time() {
			return start_time;
		}
		public void setStart_time(String start_time) {
			this.start_time = start_time;
		}
		public String getEnd_time() {
			return end_time;
		}
		public void setEnd_time(String end_time) {
			this.end_time = end_time;
		}
		public String getHeadway_secs() {
			return headway_secs;
		}
		public void setHeadway_secs(String headway_secs) {
			this.headway_secs = headway_secs;
		}
		public String getExact_times() {
			return exact_times;
		}
		public void setExact_times(String exact_times) {
			this.exact_times = exact_times;
		}
	
}
